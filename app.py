@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    data = request.get_json(force=True)
    text = data.get("text", "").strip()
    creator_id = data.get("creator_id", "unknown")
    content_type = data.get("content_type", "text")  # "text" or "image_description"

    if not text:
        return jsonify({"error": "text field is required"}), 400
    if content_type not in ("text", "image_description"):
        return jsonify({"error": "content_type must be 'text' or 'image_description'"}), 400

    content_id = str(uuid.uuid4())

    llm_score = signal_llm(text, content_type)
    stylo_score = signal_stylometric(text, content_type)
    ngram_score = signal_ngram(text)
    confidence = compute_confidence(llm_score, stylo_score, ngram_score)
    attribution = attribution_from_confidence(confidence)
    label_text = generate_label(confidence)

    # Check for provenance certificate (STRETCH)
    cert = get_certificate(creator_id)
    certificate_info = None
    if cert:
        certificate_info = {
            "badge": "Verified Human Author",
            "verified_at": cert["verified_at"],
            "note": "This creator has completed human verification for a prior submission.",
        }

    log_event({
        "content_id": content_id,
        "creator_id": creator_id,
        "content_type": content_type,
        "attribution": attribution,
        "confidence": confidence,
        "llm_score": llm_score,
        "stylo_score": stylo_score,
        "ngram_score": ngram_score,
        "status": "classified",
        "certificate": cert is not None,
    })

    response = {
        "content_id": content_id,
        "attribution": attribution,
        "confidence": confidence,
        "llm_score": llm_score,
        "stylo_score": stylo_score,
        "ngram_score": ngram_score,
        "label_text": label_text,
        "content_type": content_type,
        "status": "classified",
    }
    if certificate_info:
        response["certificate"] = certificate_info

    return jsonify(response)


