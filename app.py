@app.route("/analytics", methods=["GET"])
def analytics():
    """Return detection patterns, appeal rate, and average confidence by category."""
    all_entries = read_all_log()

    classified = [e for e in all_entries if e.get("event") != "appeal"
                  and e.get("event") != "verification"
                  and e.get("attribution") is not None]
    appeals = [e for e in all_entries if e.get("event") == "appeal"]

    total = len(classified)
    appeal_count = len(appeals)

    counts = {"likely_human": 0, "uncertain": 0, "likely_ai": 0}
    confidence_sums = {"likely_human": 0.0, "uncertain": 0.0, "likely_ai": 0.0}

    for e in classified:
        attr = e.get("attribution")
        if attr in counts:
            counts[attr] += 1
            confidence_sums[attr] += e.get("confidence", 0.0)

    detection_pattern = {}
    avg_confidence = {}
    for attr in ("likely_human", "uncertain", "likely_ai"):
        n = counts[attr]
        detection_pattern[attr] = {
            "count": n,
            "percentage": round((n / total * 100), 1) if total > 0 else 0.0,
        }
        avg_confidence[attr] = round(confidence_sums[attr] / n, 4) if n > 0 else None

    appeal_rate = round(appeal_count / total * 100, 1) if total > 0 else 0.0

    return jsonify({
        "total_submissions": total,
        "detection_pattern": detection_pattern,
        "appeal_rate": {
            "appeal_count": appeal_count,
            "percentage": appeal_rate,
        },
        "average_confidence_by_attribution": avg_confidence,
    })


