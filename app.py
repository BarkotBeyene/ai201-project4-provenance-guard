@app.route("/verify", methods=["POST"])
def verify():
    """Creator submits a verification answer about their content to earn a certificate."""
    data = request.get_json(force=True)
    content_id = data.get("content_id", "").strip()
    creator_id = data.get("creator_id", "").strip()
    verification_answer = data.get("verification_answer", "").strip()

    if not content_id or not creator_id or not verification_answer:
        return jsonify({
            "error": "content_id, creator_id, and verification_answer are all required"
        }), 400

    original = find_log_entry(content_id)
    if original is None:
        return jsonify({"error": "content_id not found"}), 404

    if original.get("creator_id") != creator_id:
        return jsonify({"error": "creator_id does not match the original submission"}), 403

    if len(verification_answer.strip()) < 20:
        return jsonify({
            "error": "verification_answer is too short. Describe something specific "
                     "about your creative process or context that only the author would know."
        }), 400

    # Store certificate keyed by creator_id
    certs = load_certificates()
    certs[creator_id] = {
        "creator_id": creator_id,
        "verified_content_id": content_id,
        "verification_answer": verification_answer,
        "verified_at": datetime.now(timezone.utc).isoformat(),
    }
    save_certificates(certs)

    log_event({
        "content_id": content_id,
        "event": "verification",
        "creator_id": creator_id,
        "status": "verified",
    })

    return jsonify({
        "creator_id": creator_id,
        "certificate": "Verified Human Author",
        "verified_content_id": content_id,
        "verified_at": certs[creator_id]["verified_at"],
        "message": "Verification complete. Your 'Verified Human Author' badge will appear "
                   "on all future content submissions.",
    })


@app.route("/certificate/<content_id>", methods=["GET"])
def certificate(content_id: str):
    """Return certificate status for a given content_id."""
    original = find_log_entry(content_id)
    if original is None:
        return jsonify({"error": "content_id not found"}), 404

    creator_id = original.get("creator_id")
    cert = get_certificate(creator_id)

    if cert:
        return jsonify({
            "content_id": content_id,
            "creator_id": creator_id,
            "certificate": "Verified Human Author",
            "verified_at": cert["verified_at"],
            "badge_text": (
                "This creator has earned a Verified Human Author certificate by "
                "completing a knowledge challenge that demonstrates personal authorship. "
                "This badge is distinct from the standard attribution analysis."
            ),
        })
    else:
        return jsonify({
            "content_id": content_id,
            "creator_id": creator_id,
            "certificate": None,
            "message": "This creator has not completed human verification.",
        })


